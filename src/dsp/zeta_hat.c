int get_zeta(double gamma, double eps, double *zeta) {
    int    index, i_gamma, i_xi;
    double gamma_dB, xi_dB, gamma_per_cell, xi_per_cell;

    gamma_dB       = 10.0 * log10_fast(gamma);
    xi_dB          = 10.0 * log10_fast(eps);
    gamma_per_cell = (gmax - gmin) / rows;
    xi_per_cell    = (ximax - ximin) / rows;
    i_gamma        = (int) floor((gamma_dB - gmin) / gamma_per_cell);
    i_xi           = (int) floor((xi_dB - ximin) / xi_per_cell);

    if (i_gamma < 0 || i_gamma >= rows || i_xi < 0 || xi_dB >= rows) {
        return -1;
    }

    index       = i_gamma * rows + i_xi;

    int ztvalue = zetaValid[index];

    if (ztvalue <= 0) {
        return -2;
    } else {
        *zeta = zetaHat[index];
    }

    return 0;
}
